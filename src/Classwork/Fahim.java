
package Classwork;



public class Fahim {
    String id;
    String age;
    String  birthdate;
    String  Abdullah_al_Fahim;

    public Fahim(String id, String age, String birthdate, String Abdullah_al_Fahim) {
        this.id = id;
        this.age = age;
        this.birthdate = birthdate;
        this.Abdullah_al_Fahim = Abdullah_al_Fahim;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getBirthdate() {
        return birthdate;
    }

    public void setBirthdate(String birthdate) {
        this.birthdate = birthdate;
    }

    public String getAbdullah_al_Fahim() {
        return Abdullah_al_Fahim;
    }

    public void setAbdullah_al_Fahim(String Abdullah_al_Fahim) {
        this.Abdullah_al_Fahim = Abdullah_al_Fahim;
    }
    

}
